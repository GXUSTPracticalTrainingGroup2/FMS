package com.ss.vv.ss.domain;

public class Food implements java.io.Serializable {
	private int cId, fId, rId, uId;
	public int getCId() {
		return cId;
	}
	public void setCId(int cId) {
		this.cId = cId;
	}
	public int getFId() {
		return fId;
	}
	public void setFId(int fId) {
		this.fId = fId;
	}
	public int getRId() {
		return rId;
	}
	public void setRId(int rId) {
		this.rId = rId;
	}
	public int getUId() {
		return uId;
	}
	public void setUId(int uId) {
		this.uId = uId;
	}
}
